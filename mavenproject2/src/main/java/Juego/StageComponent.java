package Juego;

public class StageComponent {

    private int x;
    private int y;
    private char icono;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public char getIcono() {
        return icono;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setIcono(char icono) {
        this.icono = icono;
    }
}
