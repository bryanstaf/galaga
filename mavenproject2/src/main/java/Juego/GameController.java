	public void startGame() {
        mEsc = new CreateStage(xInit, yInit);
        this.localPlayer = mEsc.getJugador();
        mEsc.iniciar();
        this.escenario = mEsc.getEscenario();
        this.setVisible(true);
        this.setTitle("Mando jugador " + (this.playerID));
    }

